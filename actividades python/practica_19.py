# Actividad 19: Mensaje de bienvenida con función
# Objetivo: Crear una función que muestre un mensaje de bienvenida

# Definimos una función llamada mostrar_bienvenida
# def introduce la definición de una función
# Esta función no tiene parámetros, por eso los paréntesis están vacíos
def mostrar_bienvenida():
    # print() muestra texto en la pantalla
    print("=" * 50)
    print("Bienvenido a las prácticas de Python")
    print("Este es un curso de administración de sistemas")
    print("=" * 50)
    print()

# Aquí comienza el programa principal
# Llamamos a la función mostrar_bienvenida()
# Los paréntesis indican que estamos ejecutando la función
mostrar_bienvenida()

# Mostramos más contenido después de la bienvenida
print("Vamos a comenzar con las actividades prácticas")
